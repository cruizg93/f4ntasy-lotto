package com.devteam.fantasy.util;

public class TuplaRiesgo {
    private Integer numero;
    private double dineroApostado;
    private double totalRiesgo;
    private double posiblePremio;

    public Integer getNumero() {
        return numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }

    public double getDineroApostado() {
        return dineroApostado;
    }

    public void setDineroApostado(double dineroApostado) {
        this.dineroApostado = dineroApostado;
    }

    public double getTotalRiesgo() {
        return totalRiesgo;
    }

    public void setTotalRiesgo(double totalRiesgo) {
        this.totalRiesgo = totalRiesgo;
    }

    public double getPosiblePremio() {
        return posiblePremio;
    }

    public void setPosiblePremio(double posiblePremio) {
        this.posiblePremio = posiblePremio;
    }
}
